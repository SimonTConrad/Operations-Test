
public class OperationsTest {

	public static void main(String[] args) 
	{
		int classic; //Declaration statement
		classic = 6; //Assignment statement -> initialization
		classic = classic + 5; // number+=5 
		System.out.println(classic);
		classic/=2;
		System.out.println(classic);
		classic-=6;
		System.out.println(classic);
		classic*=4;
		System.out.println(classic);
		classic%=2;
		System.out.println(classic);

	}

}
